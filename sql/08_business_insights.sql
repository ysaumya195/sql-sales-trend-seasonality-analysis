-- ============================ BUSINESS INSIGHTS ============================

-- Key Finding:
-- • Revenue is highly volatile and primarily driven by short-term spikes,
--   with a clear seasonal surge beginning in September and extending through Q4.

-- ---------------------------------------------------------------------------

-- • Sales performance is highly volatile at the daily level, with no consistent
--   long-term upward or downward trend observed, indicating that short-term
--   fluctuations dominate overall sales behavior.

-- • Moving average analysis confirms that, even after smoothing short-term noise,
--   no sustained trend in revenue is observed, highlighting the irregular nature
--   of daily sales patterns.

-- • Monthly growth analysis reveals frequent alternation between growth and
--   decline periods, with several instances of sharp positive and negative changes.
--   This suggests that revenue is influenced by intermittent high-performing periods
--   rather than stable month-over-month progression.

-- • The presence of extreme growth spikes indicates dependency on specific
--   high-impact periods, potentially driven by promotional campaigns,
--   seasonal demand, or bulk purchasing behavior.

-- • Seasonality analysis shows that peak sales activity begins in September,
--   marking the onset of a high-demand phase.

-- • This elevated performance continues through October and reaches its peak
--   during November and December, which record the highest seasonality indices.

-- • The pattern indicates an extended high-revenue period spanning late Q3 and Q4,
--   rather than being confined strictly to the final quarter.

-- • Overall, the dataset reflects a retail sales environment characterized by
--   high variability, event-driven performance, and a clearly defined seasonal surge
--   toward the end of the year.


-- --------------------------- SUMMARY ---------------------------

-- • Trend       → No consistent long-term stability  
-- • Growth      → Irregular and fluctuating  
-- • Seasonality → Strong, with peak demand from September to December
